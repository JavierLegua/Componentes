    private void jButtonSalirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSalirMouseClicked
        
        int opc = JOptionPane.showConfirmDialog(rootPane, "¿Estas seguro que quieres salir?");
        
        if (opc == JOptionPane.YES_OPTION) {
            exit(0);
        }
        
       
        
        
    }//GEN-LAST:event_jButtonSalirMouseClicked

    private void jRadioButtonEditarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jRadioButtonEditarMouseClicked
        
        if(jRadioButtonEditar.isSelected()){
            jTextArea1.setEnabled(false);
            Component vCom[] = jPanelUsuario.getComponents();
            for (Component c : vCom) {
                c.setEnabled(true);
            }
        }
        
    }//GEN-LAST:event_jRadioButtonEditarMouseClicked

    private void VerDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_VerDatosMouseClicked
        
        if(VerDatos.isSelected()){
            jTextArea1.setEnabled(true);
            Component vCom[] = jPanelUsuario.getComponents();
            for (Component c : vCom) {
                c.setEnabled(false);
            }
        }
        
    }//GEN-LAST:event_VerDatosMouseClicked

    private void jButtonVerDatosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonVerDatosMouseClicked
        
        //Abrir una nueva ventana
        VerDatos v = new VerDatos(this.vDatos);
        v.setVisible(true);
        this.dispose();
        
    }//GEN-LAST:event_jButtonVerDatosMouseClicked

