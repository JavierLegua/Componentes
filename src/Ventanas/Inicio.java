    private void GuardarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_GuardarMouseClicked

        String nombre, telefono, edad;
        
        if (Guardar.isEnabled()) {
        
        //Guardo los datos
        nombre = jTextFieldNombre.getText();
        telefono = jTextFieldTelefono.getText();
       
        
        vDatos.add(nombre);
        vDatos.add(telefono);
    
        
        //Reinicio los textos
        jTextFieldNombre.setText("Nombre");
        jTextFieldTelefono.setText("Telefono");
        
        //Aviso al usuario
        JOptionPane.showMessageDialog(rootPane, "Datos guardados correctamente.");
        ///JOptionPane.showMessageDialog(rootPane, "Datos guardados correctamente", "Guardado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_GuardarMouseClicked

